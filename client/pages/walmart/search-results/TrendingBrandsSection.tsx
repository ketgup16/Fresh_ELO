import { useRef, useState, useEffect } from "react";
import { InfoCircle as Info } from "@/components/icons";
import { Chip } from "@/components/ui/Chip";
import { Rating } from "@/components/ui/Rating";
import styles from "./TrendingBrandsSection.module.css";

const useDragScroll = () => {
  const ref = useRef<HTMLDivElement>(null);
  const [isDragging, setIsDragging] = useState(false);
  const [startX, setStartX] = useState(0);
  const [scrollLeft, setScrollLeft] = useState(0);
  useEffect(() => {
    const element = ref.current;
    if (!element) return;
    const handleMouseDown = (e: MouseEvent) => { setIsDragging(true); setStartX(e.pageX - element.offsetLeft); setScrollLeft(element.scrollLeft); element.style.cursor = 'grabbing'; element.style.userSelect = 'none'; };
    const handleMouseMove = (e: MouseEvent) => { if (!isDragging) return; e.preventDefault(); element.scrollLeft = scrollLeft - (e.pageX - element.offsetLeft - startX) * 2; };
    const handleMouseUp = () => { setIsDragging(false); element.style.cursor = 'grab'; element.style.userSelect = 'auto'; };
    const handleMouseLeave = () => { if (isDragging) { setIsDragging(false); element.style.cursor = 'grab'; element.style.userSelect = 'auto'; } };
    element.addEventListener('mousedown', handleMouseDown); element.addEventListener('mousemove', handleMouseMove); element.addEventListener('mouseup', handleMouseUp); element.addEventListener('mouseleave', handleMouseLeave);
    element.style.cursor = 'grab';
    return () => { element.removeEventListener('mousedown', handleMouseDown); element.removeEventListener('mousemove', handleMouseMove); element.removeEventListener('mouseup', handleMouseUp); element.removeEventListener('mouseleave', handleMouseLeave); };
  }, [isDragging, startX, scrollLeft]);
  return ref;
};

const BRANDS = ['Vizio', 'Samsung', 'LG', 'Sony', 'Hisense', 'TCL', 'Roku'];

export function TrendingBrandsSection() {
  const [selectedBrand, setSelectedBrand] = useState("Vizio");
  const brandFiltersScrollRef = useDragScroll();

  return (
    <div className={styles.section}>
      <h2 className={styles.sectionTitle}>TV brands currently trending</h2>
      <div ref={brandFiltersScrollRef} className={styles.chipRow}>
        {BRANDS.map((brand) => (
          <Chip
            key={brand}
            size="small"
            selected={selectedBrand === brand}
            onSelectedChange={() => setSelectedBrand(brand)}
          >
            {brand}
          </Chip>
        ))}
      </div>

      <div className={styles.card}>
        <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>
          <div className={styles.cardSection}>
            <p className={styles.cardLabel}>Average customer rating:</p>
            <div className={styles.ratingRow}>
              <Rating value={4.8} size="small" />
              <span className={styles.ratingLabel}>4.8 (27K ratings)</span>
            </div>
          </div>
          <div className={styles.divider} />
          <div className={styles.cardSection}>
            <p className={styles.cardLabel}>Reviews summary:</p>
            <p className={styles.bodyText}>{selectedBrand} TVs offer a good balance of picture quality and affordability.</p>
          </div>
          <div className={styles.divider} />
          <div className={styles.cardSection}>
            <p className={styles.cardLabel}>Rated by customers for:</p>
            <ul style={{ listStyle: 'none', padding: 0, margin: 0, display: 'flex', flexDirection: 'column', gap: 4 }}>
              <li className={styles.listItem}>High-quality features</li>
              <li className={styles.listItem}>User-friendly and intuitive</li>
              <li className={styles.listItem}>Value for your money</li>
            </ul>
          </div>
        </div>
      </div>

      <div className={styles.genAiRow}>
        <span className={styles.genAiLabel}>Generated by AI</span>
        <Info className="w-4 h-4 text-muted-foreground" />
      </div>
    </div>
  );
}
